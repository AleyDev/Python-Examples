"""
    Paralelkenar alanını hesapla
"""


# Paralelkenar alanı >> Paralelkenar: alan = taban * yükseklik

taban = int(input("Paralelkenarın taban uzunluğunu giriniz: "))
yukseklik = int(input("Paralelkenarın yüksekliğini giriniz: "))

alan = taban * yukseklik

print(f"Paralelkenarın alanı: {alan}")